class Person:
    def __init__(self, power, hp, energy, armor, mood, group, klan, radioactive, blood, hunger, thirst):
        self.power = power  # Переносимый вес
        self.hp = hp  # Здоровье
        self.energy = energy  # Усталость
        self.armor = armor  # Броня
        self.mood = mood  # Настроение
        self.group = group  # Группировка
        self.klan = klan  # Клан
        self.radioactive = radioactive  # Заражение радиацией
        self.blood = blood  # кровотечение
        self.hunger = hunger  # голод
        self.thirst = thirst  # обезвоживание
